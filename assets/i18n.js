export function normalizeText(value) {
  return stripAccents(String(value || ''))
    .toLowerCase()
    .replace(/[^0-9a-z\u4e00-\u9fff]+/gi, ' ')
    .replace(/\s+/g, ' ')
    .trim();
}

function singular(token) {
  const t = String(token || '');
  if (t.length > 4 && t.endsWith('ies')) return `${t.slice(0, -3)}y`;
  if (t.length > 5 && t.endsWith('aux')) return t.slice(0, -3);
  if (t.length > 4 && t.endsWith('es') && !['chips','muesli','couscous','mais','pois'].includes(t)) return t.slice(0, -2);
  if (t.length > 3 && t.endsWith('s') && !['chips','muesli','couscous','mais','pois'].includes(t)) return t.slice(0, -1);
  return t;
}

function normToken(token) {
  return singular(stripAccents(String(token || '')).toLowerCase().replace(/['’]/g, '').trim());
}

function titleize(text) {
  const trimmed = String(text || '').trim();
  if (!trimmed) return trimmed;
  return trimmed
    .split(/(\s+)/)
    .map((part) => /\s+/.test(part) ? part : (part ? part.charAt(0).toUpperCase() + part.slice(1) : part))
    .join('');
}


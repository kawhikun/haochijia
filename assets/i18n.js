export function translateNote(lang, note) {
  if (!note) return note;
  const exact = NOTE_EXACT[note];
  if (exact) return exact[lang] || exact.zh || note;
  let out = note;
  const trainingMatch = out.match(/^训练模式：(.*)，训练时长\s*(\d+(?:\.\d+)?)\s*小时\s*\/\s*周。$/);
  if (trainingMatch) {
    const trainingZh = trainingMatch[1];
    const hours = trainingMatch[2];
    let training = trainingZh;
    for (const [key, zh] of Object.entries(ENUMS.zh.training)) {
      if (zh === trainingZh) {
        training = enumLabel(lang, 'training', key);
        break;
      }
    }
    if (lang === 'en') return `Training mode: ${training}, ${hours} h/week.`;
    if (lang === 'es') return `Entrenamiento: ${training}, ${hours} h/semana.`;
  }
  const proteinFloorMatch = out.match(/^一般成年人最低参考约\s*(\d+(?:\.\d+)?)\s*g\s*\/\s*天（约\s*(\d+(?:\.\d+)?)\s*g\/kg）；当前给出\s*(\d+(?:\.\d+)?)–(\d+(?:\.\d+)?)\s*g\s*\/\s*天的实用区间。$/);
  if (proteinFloorMatch) {
    const [, floor, perkg, low, high] = proteinFloorMatch;
    if (lang === 'en') return `General adult minimum reference is about ${floor} g/day (~${perkg} g/kg). The current practical range shown is ${low}–${high} g/day.`;
    if (lang === 'es') return `La referencia mínima general para adultos es de unos ${floor} g/día (~${perkg} g/kg). El rango práctico actual es ${low}–${high} g/día.`;
  }
  const trainingProteinMatch = out.match(/^(规律健身|力量 \/ 增肌|球类 \/ 综合训练|耐力训练)常见约\s*(\d+(?:\.\d+)?)–(\d+(?:\.\d+)?)\s*g\/kg\s*\/\s*天。$/);
  if (trainingProteinMatch) {
    const labelMap = {
      '规律健身': { en: 'Regular fitness', es: 'Fitness regular' },
      '力量 / 增肌': { en: 'Strength / hypertrophy', es: 'Fuerza / hipertrofia' },
      '球类 / 综合训练': { en: 'Mixed / sport', es: 'Mixto / deporte' },
      '耐力训练': { en: 'Endurance', es: 'Resistencia' },
    };
    const [, mode, low, high] = trainingProteinMatch;
    const modeLabel = labelMap[mode]?.[lang] || mode;
    if (lang === 'en') return `${modeLabel} commonly lands around ${low}–${high} g/kg/day.`;
    if (lang === 'es') return `${modeLabel} suele situarse alrededor de ${low}–${high} g/kg/día.`;
  }
  return normalizeValueForLang(out, lang);
}


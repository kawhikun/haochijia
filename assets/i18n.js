function normalizeValueForLang(value, lang) {
  const raw = String(value || '').trim();
  if (!raw) return raw;
  const exact = NOTE_EXACT[raw];
  if (exact) return exact[lang] || exact.zh || raw;

  let out = raw;
  out = out.replace(/(\d+(?:\.\d+)?)\s*小时\s*\/\s*周/g, (_, n) => lang === 'es' ? `${n} h/semana` : `${n} h/week`);
  out = out.replace(/(\d+(?:\.\d+)?)\s*g\s*\/\s*天/g, (_, n) => lang === 'es' ? `${n} g/día` : `${n} g/day`);
  out = out.replace(/(\d+(?:\.\d+)?)\s*kcal/g, (_, n) => `${n} kcal`);
  out = out.replace(/当前无专项训练/g, lang === 'es' ? 'Sin entrenamiento específico ahora' : 'No dedicated training currently');

  if (lang !== 'zh') {
    Object.entries(ENUMS.zh.activity).forEach(([key, zh]) => {
      out = out.replaceAll(zh, enumLabel(lang, 'activity', key));
    });
    Object.entries(ENUMS.zh.training).forEach(([key, zh]) => {
      out = out.replaceAll(zh, enumLabel(lang, 'training', key));
    });
    Object.entries(ENUMS.zh.physiology).forEach(([key, zh]) => {
      out = out.replaceAll(zh, enumLabel(lang, 'physiology', key));
    });
    Object.entries(ENUMS.zh.glucose).forEach(([key, zh]) => {
      out = out.replaceAll(zh, enumLabel(lang, 'glucose', key));
    });
  }
  return out;
}


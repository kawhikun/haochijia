function chooseZhLabel(food, generated, original) {
  const base = cleanupFoodLabel(food?.z || '', 'zh');
  const generatedClean = cleanupFoodLabel(generated || '', 'zh');
  const descriptors = extractZhDescriptors(food, original);
  const sanitizedGenerated = cleanupFoodLabel(generatedClean.replace(/[A-Za-zÀ-ÿ][A-Za-zÀ-ÿ0-9'’.-]*/g, ' ').replace(/\s{2,}/g, ' '), 'zh');
  let enhanced = sanitizedGenerated || generatedClean || base;
  const carrier = enhanced || base;
  const genericBase = /^(虾|大虾|对虾|龙虾|鱼|肉|奶|面|饭|沙拉|汤|饺子)$/.test(base);
  const nounMatch = carrier.match(/(龙虾|对虾|大虾|虾片|虾饺|虾尾|虾|鱼汤|汤|沙拉|面|饭|饺子|春卷|卷)$/);
  if (descriptors.length && nounMatch) {
    const noun = nounMatch[1];
    enhanced = cleanupFoodLabel(`${descriptors.join(' ')} ${noun}`, 'zh');
  } else if (descriptors.length && !enhanced) {
    enhanced = cleanupFoodLabel(descriptors.join(' '), 'zh');
  }
  if (!base) return enhanced || original;
  if (!genericBase && !hasLatinText(base) && (hasLatinText(generatedClean) || labelInfoScore(base) >= labelInfoScore(enhanced) - 1)) return base;
  if (genericBase && descriptors.length && nounMatch) return enhanced || base || original;
  return labelInfoScore(enhanced) > labelInfoScore(base) + 1 ? enhanced : (base || enhanced || original);
}


